// declare the setup function
function setup() {
  // creation of canvas with parameters: 600 px wide and 400 px high
  createCanvas(600, 400);
  // choose background color for newly created canvas
  background('green');
}

// declare the draw function
function draw() {
  // use 'text()' function to print string: Hello World!
  // include parameters for string position on canvas
  text("Hello World!", 20, 30);
}
